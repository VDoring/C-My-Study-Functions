#include <stdio.h>

int main() {
	unsigned char a = 4 << 1; // 0000 0100 ( << 1이 붙었으므로 윈쪽으로 1이 한칸 이동.  즉 8 )
	unsigned char b = 8 << 2; // 0000 1000 ( << 2가 붙었으므로 왼쪽으로 1이 두칸 이동.  즉 32 )

	printf("4 << 1 : %d\n", a);
	printf("8 << 2 : %d\n", b);

	a = 6 << 3;  // 0000 0110 ( 0011 0000 = 48 )
	b = 10 << 4; // 0000 1010 ( 1010 0000 = 160 )

	printf("6 << 3 : %d\n", a);
	printf("10 << 4 : %d\n", b);

}